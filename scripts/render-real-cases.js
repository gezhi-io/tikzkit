import { mkdir, readFile, rm, writeFile } from "node:fs/promises";
import { spawnSync } from "node:child_process";
import path from "node:path";
import { tikzToSvg } from "../src/index.js";
import { PACKT_CASES, PACKT_ROOT } from "./packt-real-cases.js";

const outputRoot = "outputs/real-cases";
const texRoot = path.join(outputRoot, "latex");
const svgRoot = path.join(outputRoot, "svg");
const referenceRoot = path.join(outputRoot, "reference-png");

await rm(outputRoot, { recursive: true, force: true });
await mkdir(texRoot, { recursive: true });
await mkdir(svgRoot, { recursive: true });
await mkdir(referenceRoot, { recursive: true });

const rows = [];
for (const [index, relativePath] of PACKT_CASES.entries()) {
  const id = `${String(index + 1).padStart(2, "0")}-${path.basename(relativePath, ".tex")}`;
  const sourcePath = path.join(PACKT_ROOT, relativePath);
  const source = await readFile(sourcePath, "utf8");

  const ours = tikzToSvg(source);
  const svgPath = path.join(svgRoot, `${id}.svg`);
  await writeFile(svgPath, ours.svg, "utf8");

  const texWorkDir = path.join(texRoot, id);
  await mkdir(texWorkDir, { recursive: true });
  const texPath = path.join(texWorkDir, "case.tex");
  await writeFile(texPath, source, "utf8");

  const latex = spawnSync("xelatex", ["-interaction=nonstopmode", "-halt-on-error", "case.tex"], {
    cwd: texWorkDir,
    encoding: "utf8"
  });
  let referencePath = "";
  let latexOk = latex.status === 0;
  if (latexOk) {
    referencePath = path.join(referenceRoot, id);
    const raster = spawnSync("pdftocairo", ["-png", "-singlefile", "-r", "144", path.join(texWorkDir, "case.pdf"), referencePath], {
      encoding: "utf8"
    });
    latexOk = raster.status === 0;
    referencePath = `${referencePath}.png`;
  }

  rows.push({
    id,
    source: relativePath,
    svg: svgPath,
    reference: referencePath,
    diagnostics: ours.diagnostics,
    irItems: ours.ir.items.length,
    latexOk
  });
}

const reportPath = path.join(outputRoot, "report.md");
await writeFile(reportPath, renderReport(rows), "utf8");
await writeFile(path.join(outputRoot, "report.json"), `${JSON.stringify(rows, null, 2)}\n`, "utf8");
process.stdout.write(`Wrote ${rows.length} real-case comparisons to ${outputRoot}\n`);

function renderReport(rows) {
  const lines = [
    "# TikZ Real-Case Rendering Report",
    "",
    "The SVG files are generated by the JavaScript interpreter. The PNG files are generated by local `xelatex` plus `pdftocairo` for visual reference.",
    "",
    "| # | Source | JS SVG | XeTeX PNG | Diagnostics |",
    "| - | - | - | - | - |"
  ];
  for (const row of rows) {
    lines.push(
      `| ${row.id} | ${row.source} | ${row.svg} | ${row.latexOk ? row.reference : "compile failed"} | ${
        row.diagnostics.length ? row.diagnostics.map((item) => item.message).join("<br>") : "none"
      } |`
    );
  }
  lines.push("");
  return lines.join("\n");
}
