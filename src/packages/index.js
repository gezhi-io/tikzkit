function normalizeTexPackage(pkg) {
  return Object.freeze({
    ...pkg,
    features: Object.freeze([...(pkg.features || [])]),
    requires: Object.freeze([...(pkg.requires || [])]),
    caseExamples: Object.freeze([...(pkg.caseExamples || [])]),
    observedOptions: Object.freeze([...(pkg.observedOptions || [])])
  });
}

